# 1. Does list maintain insertion order?

my_list = [10, 20, 30, 40]
print("Original list:", my_list)


# 2. Can we access list items with negative index?

my_list = [10, 20, 30, 40]
print("Last item using negative index:", my_list[-1])  # Output: 40
print("Second last item:", my_list[-2])  # Output: 30

# 3. Which function empties the list?

my_list = [1, 2, 3, 4]
my_list.clear()
print("List after clear():", my_list)  # Output: []




